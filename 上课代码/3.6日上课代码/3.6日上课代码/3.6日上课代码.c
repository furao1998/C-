//实型（浮点数）表示的数据是无法保证准确性
//实型（浮点数）没法直接判等或判0
#include <stdio.h>
int main(void)
{
    float a=1e^-20;/*<0.00000000001>*/
    float b=-1e^-20;
    printf ("%f\n",a);
    printf ("%f\n",b);

    if(a==b)
       printf ("相等\n");/*比较大小时先判断符号位在判断阶码在判断尾码*/
    else
         printf ("不相等\n");/*（若没有符号位是是否可以判断这两个的大小吗）
*/
    return 0;
}
