//阶码：移码  表示的是2进制科学计数法次幂+127（127为2^8-1）（对于float）
//            对于（double）+(2^11-1)
//            补码：-128~127
//            移码：-127~128
//尾码23位（小数位为23位但是还有整数位所以有效表示的为24位）
